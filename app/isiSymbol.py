# Definicao de simbolos utilizada pelo compilador de IsiLang



class IsiSymbol:
    
    def __init__(self, nome):
        self.nome = nome

    def getName(self):
        self.name

    def setName(self, newname):
        self.name = newname

    def toString(self):
        return "IsiSymbol [name = " + self.name + " ]"

